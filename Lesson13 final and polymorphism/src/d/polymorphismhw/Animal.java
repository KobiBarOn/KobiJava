package d.polymorphismhw;

public class Animal {

	private double weight;
	private String color;

	public Animal(double weight, String color) {
		super();
		this.weight = weight;
		this.color = color;
	}

	public double getWeight() {
		return weight;
	}

	public void setWeight(double radius) {
		this.weight = weight;
	}

	public String getColor() {
		return color;
	}

	public void setColor(double radius) {
		this.color = color;
	}
}